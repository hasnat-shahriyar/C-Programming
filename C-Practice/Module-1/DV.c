// Data Types and Variables
#include <stdio.h>
#include <stdbool.h>

int main() {
    // Integer variable
    int integerVar = 100;
    printf("Integer Variable: %d\n", integerVar);

    // Float variable
    float floatVar = 5.45;
    printf("Float Variable: %.2f\n", floatVar);

    // Character variable
    char charVar = '@';
    printf("Character Variable: %c\n", charVar);

    // Boolean variable
    bool boolVar = true;
    printf("Boolean Variable: %s\n", boolVar ? "true" : "false");

    return 0;
}

/*
Example of Data Types :
int -> -100, 0, 100
float -> -2.5, 5.45
char -> "a", 'A', '@'  <- [It must be]
bool -> true or false
*/