#include <stdio.h>

int main(int argc, char const *argv[])
{
    // Date Calculation
    int currentDate, birthDate;
    currentDate = 2024;
    birthDate = 2000;
    printf("My age is : %d\n", currentDate - birthDate);

    // Multiple Operators
    int a, b;
    a = 10;
    b = 5;

    printf("a + b = %d\n", a + b);
    printf("a - b = %d\n", a - b);
    printf("a * b = %d\n", a * b);
    printf("a / b = %d\n", a / b);

    return 0;
}
